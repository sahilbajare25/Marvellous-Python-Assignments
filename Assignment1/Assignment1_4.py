def main():                     # here main function body is written 
    i = 0                       # here one local variable is 'i' taken and store with zero value for counter
    while i<5:                  # here while loop is written which check whether local variable 'i' is less than 5 or not
        print("Marvellous")     # here if condition return True then code gets inside while loop and print "Marvellous"
        i = i + 1               # here local variable 'i' which is used as counter gets incrementd by 1 

if __name__ == "__main__":      # code start from here
    main()                      # here main function get call and code jump to line 1