def main():                         # here main function body is written
    for no in range(10,0,-1):       # here for loop is used to retrive data from range function which start with 10 and decrement 1 on every loop until no reach 0 and for loop stop
        print(no, end=" ")          # here data from no is print with end=" " written to print all values on same line


if __name__ == "__main__":          # code start from here
    main()                          # main function get call from here and code jump to line 1